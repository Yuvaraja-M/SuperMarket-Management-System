    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(RoleCB.getSelectedItem().toString().equals("Seller")) {
            String Query = "select * from Vignesh.SellerTable where SellerName = '" + UserID.getText() + "' and SellerPass = '" + Password.getText() + "'";
            try {
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/SupermarketDB", "Vignesh", "Vignesh");
                St = Con.createStatement();
                Rs = St.executeQuery(Query);
                if(Rs.next()) {
                    new Billing().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Wrong Seller ID or Password");
                }
            } catch(Exception e) {
                e.printStackTrace();
            }
        } else {
            String Query = "select * from Vignesh.AdminTable where AdminName = '" + UserID.getText() + "' and AdminPass = '" + Password.getText() + "'";
            try {
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/SupermarketDB", "Vignesh", "Vignesh");
                St = Con.createStatement();
                Rs = St.executeQuery(Query);
                if(Rs.next()) {
                    new Products().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Wrong Admin ID or Password");
                }
            } catch(Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

